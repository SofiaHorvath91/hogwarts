        <h4>DOING SOME PRACTICE - THE HOMEWORK</h4>
        Shortly after the classes, during the evening, a dinner is served in the Great Hall, and then students usually
        attack their homeworks, which can be writing essay, practicing magic, keeping follow-up diary about an
        assignemt, reading chapters or collecting information for next lesson. Depending on their tasks, students can do
        their studies in the Study Hall (under the supervision of a teacher), in the Library or in the Common Room of
        their House along with their peers.
