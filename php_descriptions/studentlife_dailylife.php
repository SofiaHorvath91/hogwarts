        <br><br>
        <h4>HAVING SOME FUN - THE EVENING TIME</h4>
        After the classes and the homeworks, students have many possibilities to take a little break and have some fun :
        they can join various clubs or teams and then visit the regular huddles, spend time and have fun with friends
        from the same or any other House, or just simply relax with a good book. There are always few students whose
        evenings are less entertaining - for breaking of rules, the detention is often some extra task for the evenings
        or weekends, under the supervision of a teacher.
        <br>Though the students are expected to be in the Common Rooms by a certain time, the times are different for
        different years, and younger students can also have some extra time for special occassions with the permission
        from a teacher.
