        <h4>STARTING THE DAY - THE MORNING</h4>
        The day begins at 7:30 a.m. with breakfast in the Great Hall.
        Students usually arrive already in the uniform of their House, since it is the expected outfit in the classes.
        During breakfast, the mail arrives in a flurry of hundreds of owls.
        <br>After collecting some snacks and the news, the students hurry through the corridors to arrive in time on the
        first class, which starts at 9 AM.
        A bell chime signals the start of the first class, and then it chimes again in one hour to signal the start of
        the next class.
    </div>
