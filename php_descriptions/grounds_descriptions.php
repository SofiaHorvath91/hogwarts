        <div class="grounds-icon" id="station" name="station">Hogsmeade Station</div>
        <div class="row container-row">
            <br>Hogsmeade station is the platform where the Hogwarts Express ends its journey north from Platform
            Nine-And-Three-Quarters. It serves both the village of Hogsmeade and Hogwarts School.
            <br>The station is located near the Black Lake, and from there a road extends around the lake to Hogwarts
            Castle. It is actually quite the distance from the village itself, being on the opposite side of the
            Hogwarts grounds. At the end of the platform, boats are docked awaiting the arrival of Groundskeeper to take
            the First years to the castle across the lake. For the students of Year Two - Year Seven, "horseless"
            carriages (actually pulled by Thestrals) are lining up next to the station for reaching the castle by a
            small road around the lake.<br><br>
