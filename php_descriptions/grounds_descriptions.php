    <div class="col-12">
        <div class="grounds-icon" id="forest" name="forest">Forbidden Forest</div>
        <div class="row container-row">
            <br>The Forbidden Forest, also known as the Dark Forest, borders the edges of the Hogwarts Grounds.
            The forest is a very old place that holds many secrets and houses many creatures, some dark and dangerous,
            others friendly. It is thick with dense, ancient trees — beech, oak, pine, sycamore, and yew are mentioned,
            as well as undergrowth such as knotgrass and thorns — but there are paths, brooks, and some clearings. Off
            the path the way is almost impassable.
            <br>The forest is home to a wide and strange assortment of magical creatures beside the normal, non-magical
            inhabitants - anything wild and/or dangerous that needs a place to live is put there.
            The forest's magical population includes unicorns, acromantulas, thestrals, centaurs, trolls and werewolfs,
            and even a wild flying Ford Anglia. There are areas around the edges of the forest that are part of the
            Hogwarts grounds, but which are out of sight of the castle.
            <br>As the forest's name suggests, it is strictly off limits to students — except in the case of detention,
            or Care of Magical Creatures lessons that occasionally take place there.<br><br>
