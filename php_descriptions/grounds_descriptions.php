        <div class="grounds-icon" id="quidditch" name="quidditch">Quidditch Grounds : Quidditch Stadium & Practice Pitch
        </div>
        <div class="row container-row">
            <br>Hogwarts has its own Quidditch Stadium where Quidditch teams could practise, hold try-outs and play
            matches against each other. Each year will see a total of six inter-house matches (each house competing for
            the Quidditch Cup), along with numerous training sessions by each house team.
            <br>The stands surrounding the pitch are decorated differently for each Quidditch match. Every second stand
            would be decorated with the colours of one team, and every other stand with the colours of the opposing
            team. Spectators would sit in between these stands.
            <br>Not far from the Stadium, there is also the flat, short-cut grassy Practice Pitch where Flying classes
            are taught to first years.
            <br>By the Stadium and the Practice is, you can find the broomshed, a small wooden building in which school
            brooms are stored, and where students can store their own broomsticks.<br><br>
