        <div class="grounds-icon" id="willow" name="willow">Whomping Willow</div>
        <div class="row container-row">
            <br>The Whomping Willow is a valuable, very violent species of magical plant. It will attack anyone and
            anything that comes within range of its branches. A deciduous plant, its limbs function as arms and any
            damage to them must be treated in much the same way.
            <br>The most famous Whomping Willow is the one planted on the grounds of Hogwarts. The Whomping Willow was
            planted around the year 1971 to disguise the opening of a secret passage leading from the Hogwarts grounds
            to the Shrieking Shack in the village of Hogsmeade.<br><br>
