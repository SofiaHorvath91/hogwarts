        <div class="grounds-icon" id="gate" name="gate">Entrance Gates</div>
        <div class="row container-row">
            <br>The Entrance Gates are made of magnificent wrought iron and are flanked by two columns topped with
            statues of winged boars. The winged hogs on either side of the Hogwarts gate are a fanciful version of the
            old saying "When pigs fly," meaning something that is impossible. To Muggles, magic is impossible, but not
            at Hogwarts. Just a few yards from the gates is a small guardhouse with a tall chimney.
            As the all of the Hogwarts Grounds are protected by powerful magic, the Entrace Gates is the main guarded
            entry point for visitors. Students also leave here to visit Hogsmeade village.<br><br>
