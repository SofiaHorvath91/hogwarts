    <div class="col-12">
        <div class="grounds-icon" id="hogsmeade" name="hogsmeade">Hogsmead Village</div>
        <div class="row container-row">
            <br>Hogsmeade Village, or simply called Hogsmeade, is the only all-wizarding village in Britain.
            According to tradition, Hogsmeade was founded over a thousand years ago, around the same time as the school,
            by medieval wizard Hengist of Woodcroft as he was fleeing persecution by Muggles. It is a picturesque little
            village of cottages and shops, with enchanted candles hanging in the trees during the holidays. It is also
            near the location of the train station used by the Hogwarts Express.
            <br>Since 1714 (when the 1714 Edict was passed), Hogwarts third years and above have been permitted weekend
            trips into the village. Mainly, students frequent the High Street in the village which contains the named
            specialty shops and pubs such as Zonko's Joke Shop and Honeydukes. Otherwise, they wander on to observe the
            infamous Shrieking Shack.<br><br>
        </div>
