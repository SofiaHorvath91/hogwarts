<div class="row container-row">
    <div class="col-12">
        <div class="grounds-icon" id="castle" name="castle">Hogwarts Castle</div>
        <div class="row container-row">
            <br>Hogwarts Castle is a large, seven-story high building supported by magic, with a hundred and forty two
            staircases throughout its many towers and turrets and very deep dungeons.It was built in the late Early
            Middle Ages (c. 993) by a wizard architect and the four most celebrated wizards of the age: Godric
            Gryffindor, Helga Hufflepuff, Rowena Ravenclaw and Salazar Slytherin.
            <br>The castle is the main building of Hogwarts School of Witchcraft and Wizardry, regarded as the finest
            wizarding school in the world. It is supported by magic (unable to be maintained or constructed by any other
            means) and also protected by numerous ancient spells.
            <br>Hogwarts is located in the Scottish Highlands, near the all-wizard village of Hogsmeade and not far from
            Dufftown, in Banffshire, and Achintee, in Lochaber. It was built in a valley area — surrounding mountains
            are part of the landscape — with the fairly large Great Lake to the south of the main building. The huge
            main oak front doors leading into the Entrance Hall face the west, and open up to sloping lawns. The deep
            Forbidden Forest extends around to the west of the castle. There are also exterior greenhouses and vegetable
            patches on the school grounds.<br><br>
