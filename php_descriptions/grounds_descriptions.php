        <div class="grounds-icon" id="lake" name="lake">Black Lake</div>
        <div class="row container-row">
            <br>The Black Lake, alternatively known as the Great Lake, is the large, cold lake, freshwater and
            landlocked, located to the south of Hogwarts Castle.
            It is about half a mile in diameter and the castle's plumbing network drains into it.
            <br>As the grounds and waters of Hogwarts function partly as a nature reserve for magical creatures
            which have difficulty existing in Muggle-inhabited areas,
            there are Grindylows (vicious little water demons), Selkies (of a hardy Scottish strain) and also a
            giant squid in the lake. This latter one is semi-domesticated and permits students to tickle its tentacles
            on sunny days, when it basks in the shallows. The giant squid also serves as occasional life-saver for
            students who fall in the lake.
            <br>The Black Lake also gives home to the Black Lake Merpeople colony, consisting of hundreds of
            merpeople, who are just rarely seen above, but helps to keep control over the beasts of the
            lake.<br><br>
