    <div class="col-xs-1 text-center">
        <h4 class="subjects-sub-title">THIRD YEARS AND UP</h4>
    </div>
</div>
<div class="row container-row">
    <div class="col-12">
        <div class="subjects-sub-icon">Arithmancy</div>
        <div class="row container-row">
            Arithmancy is a branch of magic concerned with the magical properties of numbers. It includes various subjects, such as numerology and predicting the future using numbers. Arithmancy is a difficult subject, as it requires memorising or working with many charts.
        </div>
    </div>
</div>
<div class="row container-row">
    <div class="col-12">
        <div class="subjects-sub-icon">Care of Magical Creatures</div>
        <div class="row container-row">
            This class instructs students on how to care for magical beasts. They learn about a wide range of magic creatures, and also about their proper feeding, maintaining, breeding, and treatment. Classes are held outside the castle, sometimes in the Forbidden Forest.
        </div>
    </div>
</div>
<div class="row container-row">
    <div class="col-12">
        <div class="subjects-sub-icon">Divination</div>
        <div class="row container-row">
            Divination is the art of predicting the future. Various methods are included, including tea leaves, Fire-omens, Crystal balls, Palmistry, Cartomancy (including the reading of conventional playing cards and the tarot), astrology, and Dream interpretations. 
        </div>
    </div>
</div>
<div class="row container-row">
    <div class="col-12">
        <div class="subjects-sub-icon">Muggle Studies</div>
        <div class="row container-row">
            This class involves the study of the Muggle (non-magical) culture from a wizarding point of view, including Muggle Art & Music. It studies the history / daily lives of Muggles, and it's aim is to ensure that witches and wizards can blend in with them if needed.
        </div>
    </div>
</div>
<div class="row container-row">
    <div class="col-12">
        <div class="subjects-sub-icon">Study of Ancient Runes</div>
        <div class="row container-row">
            The Study of Ancient Runes, or Runology, is the study of runic scriptures. Ancient Runes are a form of writing used by witches and wizards hundreds of years ago. Their study is mostly a theoretical subject, aiming to translate the ancient magical runic scripts.
        </div>
    </div>
</div>

<div class="row container-row">
    <div class="col-xs-1 text-center">
        <h4 class="subjects-sub-title">SIXTH AND SEVENTH YEARS</h4>
    </div>
</div>
<div class="row container-row">
    <div class="col-12">
        <div class="subjects-sub-icon">Advanced Arithmancy Studies</div>
        <div class="row container-row">
            Advanced Arithmancy Studies is Arithmancy for students with particular skill at the subject, aiming to prepare them for the highest level of exams.
        </div>
    </div>
</div>
<div class="row container-row">
    <div class="col-12">
        <div class="subjects-sub-icon">Alchemy</div>
        <div class="row container-row">
            Alchemy is a sort of composite subject between Transfiguration, Potions and Muggle Chemistry, focused roughly on the transmutation of substances into other forms.
        </div>
    </div>
</div>
<div class="row container-row">
    <div class="col-12">
        <div class="subjects-sub-icon">Ancient Studies</div>
        <div class="row container-row">
            This subject is a specialised branch of History of Magic, the magical equivalent of cultural, social and/or linguistic anthropology. It focuses on ancient magic, such as ancient Egyptians' spells, and very useful for future Curse-Breakers.
        </div>
    </div>
</div>
<div class="row container-row">
    <div class="col-12">
        <div class="subjects-sub-icon">Apparition</div>
        <div class="row container-row">
            Apparition is the magical form of teleportation in the wizarding world. Performing Apparition requires a licence and may only be legally performed by people over seventeen years of age.
        </div>
    </div>
</div>

