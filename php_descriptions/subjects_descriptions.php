<div class="row container-row">
    <div class="col-12">
        <div class="subjects-sub-icon">Astronomy</div>
        <div class="row container-row">
            Astronomy is the only field of study at Hogwarts that has a direct equivalent in the Muggle world. Lessons involve observations of the night skies with telescopes, togather with learning the names of stars, constellations and planets, and their location, movements, and environments. Astronomy classes take place in the Astronomy Tower, the tallest tower in Hogwarts. Lessons are held every week at midnight, and first years have lessons on Wednesday. 
        </div>
    </div>
</div>
<div class="row container-row">
    <div class="col-12">
        <div class="subjects-sub-icon">Charms</div>
        <div class="row container-row">
            Charms is the type of spells concerned with giving an object new and unexpected properties, and hence this class mainly consists on learning those sorts of spells. Mastering the science of charm-work is essential to performing the greater part of magic.The spells learned in Charms class are taken from textbooks. Students are taught specific wand movements and proper pronunciation. Often students partner up in class to experiment on one another.
        </div>
    </div>
</div>
<div class="row container-row">
    <div class="col-12">
        <div class="subjects-sub-icon">Defence Against the Dark Arts</div>
        <div class="row container-row">
            Defence Against the Dark Arts, commonly shortened to D.A.D.A., is the class that teaches students defensive techniques against the Dark Magic and the Dark creatures. In this class, students study and learn how to defend themselves against all aspects of the Dark Arts, including dark creatures, curses, hexes and jinxes (dark charms), and duelling. The lessons generally have a practical approach with theoretical assignments given as homework.
        </div>
    </div>
</div>
<div class="row container-row">
    <div class="col-12">
        <div class="subjects-sub-icon">Flying</div>
        <div class="row container-row">
            Flying is taught only to first year students, and teaches the use of broomsticks made for the use of flying. The aim is to provide students with the most elementary principles of broomology : how to handle a broom, basic flight techniques and proper broom maintenance. This is the only subject that requires physicality.
        </div>
    </div>
</div>
<div class="row container-row">
    <div class="col-12">
        <div class="subjects-sub-icon">Herbology</div>
        <div class="row container-row">
            Herbology is the the wizarding equivalent to botany, the study of magical plants. Students spend class time learning about how to take care of, utilise and combat them. There are three greenhouses, holding a variety of magical plants of varying degrees of lethality. The further into a student's education the more difficult and dangerous the plants become. Many plants provide ingredients for potions and medicine, while others have magical effects of their own right.
        </div>
    </div>
</div>
<div class="row container-row">
    <div class="col-12">
        <div class="subjects-sub-icon">History of Magic</div>
        <div class="row container-row">
            This class is a study of magical history. The lesson plan usually consists of lectures on the 'History of Wizards and the Magical World'. It is quite similar to the study of History in the Muggle World, as particular emphasis is placed upon remembering dates, names and events. This is one of the subjects where the use of magic practically isn't necessary. Topics have included Goblin Rebellions, Giant wars, and the origins of wizarding secrecy. 
        </div>
    </div>
</div>
<div class="row container-row">
    <div class="col-12">
        <div class="subjects-sub-icon">Potions</div>
        <div class="row container-row">
            Potions is described as the art of creating mixtures with magical effects. It requires the correct mixing and stirring of ingredients at the right times and temperatures. On the lessons, students learn the correct way to brew potions. They follow specific recipes and use various magical ingredients to create the potions, starting with simple ones and moving to more advanced ones as they progress in knowledge. A standard potions kit includes plant ingredients and supplies, along with a standard cauldron.
        </div>
    </div>
</div>
<div class="row container-row">
    <div class="col-12">
        <div class="subjects-sub-icon">Transfiguration</div>
        <div class="row container-row">
            Transfiguration is the art of changing the form or appearance of an object or of a living being, a theory-based subjec, considered both complex and dangerous. It includes various topics such as "Switching Spells" (altering only a part of some object), Vanishing Spells (causing an object to disappear), and Conjuring Spells (creating objects out of thin air). There are also many branches, including Cross-Species Transfiguration and Human Transfiguration.
        </div>
    </div>
</div>

